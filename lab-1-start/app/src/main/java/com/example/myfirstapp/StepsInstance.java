package com.example.myfirstapp;

public class StepsInstance {

    private String time;
    private int steps;

    public StepsInstance() {
        // blank constructor
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public int getSteps() {
        return steps;
    }

    public void setSteps(int steps) {
        this.steps = steps;
    }
}
